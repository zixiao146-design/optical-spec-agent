def _preview_postprocess_block(m: MeepInputModel) -> str:
    """Generate post-processing code for preview resonance/FWHM extraction."""
    if not m.postprocess:
        return ""

    lines = ["# =====================================================================",
             "# Post-processing: resonance / FWHM extraction",
             "# ====================================================================="]

    if "resonance_wavelength" in m.postprocess:
        lines.append("""
# Find resonance peak
peaks, props = find_peaks(flux, height=max(flux) * 0.5)
if len(peaks) > 0:
    res_idx = peaks[np.argmax(props["peak_heights"])]
    res_wl = wl_nm[res_idx]
    print(f"Resonance wavelength: {res_wl:.1f} nm")
else:
    print("No clear resonance peak found")
""")

    if "fwhm_extraction" in m.postprocess:
        lines.append("""
# FWHM estimation (simplified half-max method)
if len(peaks) > 0:
    peak_flux = max(flux)
    half_max = peak_flux / 2.0
    above = np.where(np.array(flux) >= half_max)[0]
    if len(above) > 1:
        fwhm = wl_nm[above[-1]] - wl_nm[above[0]]
        print(f"FWHM: {fwhm:.1f} nm")
    else:
        print("Could not estimate FWHM")
""")

    if "T2_extraction" in m.postprocess:
        lines.append("""
# T2 (dephasing time) from FWHM: T2 = 2ℏ / Γ, where Γ = ℏω·FWHM/λ²
# Simplified: T2 ≈ 2λ² / (2πc·FWHM)
if len(peaks) > 0 and len(above) > 1:
    import scipy.constants as const
    res_wl_m = res_wl * 1e-9
    fwhm_m = fwhm * 1e-9
    T2 = 2 * res_wl_m**2 / (2 * np.pi * const.c * fwhm_m)
    print(f"T2 (dephasing time): {T2*1e15:.1f} fs")
""")

    return "\n".join(lines)


