def _research_limitations(m: MeepInputModel) -> list[str]:
    limitations = [
        "Research-preview only; not production-grade or publication-ready.",
        "Uses particle-induced flux relative to a film/background reference run.",
        "Peak extraction is heuristic and may return null when no clear resonance is detected.",
        "Gap/background fallback may use a constant-index mp.Medium(epsilon=n**2).",
    ]
    if m.boundary_type == "pml" and m.material_mode == "library":
        limitations.append(
            "PML with dispersive library metals may be numerically unstable in some Meep setups."
        )
    if m.boundary_type == "absorber":
        limitations.append(
            "Absorber boundary is a stability diagnostic workaround and may change quantitative results."
        )
    if m.material_mode == "dielectric_sanity":
        limitations.append(
            "Dielectric sanity material mode is nonphysical and should not be interpreted as a metal scattering result."
        )
    if m.material_mode in ("particle_library_film_dielectric", "particle_dielectric_film_library"):
        limitations.append(
            f"{m.material_mode} is a mixed-material stability isolation probe and is not a validated physical model."
        )
    if m.diagnostic_profile == "low_cost":
        limitations.append(
            "diagnostic_profile=low_cost uses low resolution, few frequency points, and fixed short runs; it validates execution plumbing only."
        )
    if m.diagnostic_profile == "physical_probe":
        limitations.append(
            "diagnostic_profile=physical_probe is a v0.6 pre-study setting, not production validation."
        )
    if m.flux_mode == "single_plane":
        limitations.append(
            "flux_mode=single_plane is diagnostic only and not a scattering cross-section."
        )
    if m.flux_mode == "top_plane":
        limitations.append(
            "top_plane flux is diagnostic only and not a closed scattering cross-section."
        )
    if m.sweep_variable:
        limitations.append(
            "If the input spec contains a sweep, this research-preview script renders a single nominal geometry rather than a full automated sweep pipeline."
        )
    return limitations
