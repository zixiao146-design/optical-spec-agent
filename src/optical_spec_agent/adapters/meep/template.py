def render_script(m: MeepInputModel) -> str:
    """Render a MeepInputModel into a Python script."""
    mode = getattr(m, "script_mode", "preview")
    if getattr(m, "smoke", False) or mode == "smoke":
        return _render_smoke(m)
    if mode == "research_preview":
        return _render_research_preview(m)
    return _render_preview(m)


