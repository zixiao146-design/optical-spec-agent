def make_flux_regions(box_center, box_size):
    return [region for _name, region in make_flux_region_specs(box_center, box_size)]


def flux_surface_names():
    return [name for name, _region in make_flux_region_specs(box_center, box_size)]


def add_closed_box_flux(sim):
    return [
        sim.add_flux(fcen, df, nfreq, region)
        for region in make_flux_regions(box_center, box_size)
    ]


def run_reference():
    sim_ref = build_simulation(include_particle=False)
    flux_ref = add_closed_box_flux(sim_ref)
    if stop_strategy == "fixed":
        # Diagnostic/probe fixed run: bounded, not necessarily physically converged.
        sim_ref.run(until=fixed_run_until)
    else:
        sim_ref.run(
            until_after_sources=mp.stop_when_fields_decayed(
                decay_check_interval, mp.Ez, box_center, decay_threshold
            )
        )
    ref_flux_data = [sim_ref.get_flux_data(flux_obj) for flux_obj in flux_ref]
    freqs = mp.get_flux_freqs(flux_ref[0])
    sim_ref.reset_meep()
    return freqs, ref_flux_data


def run_structure(ref_flux_data):
    sim_scat = build_simulation(include_particle=True)
    flux_scat = add_closed_box_flux(sim_scat)
    for flux_obj, flux_data in zip(flux_scat, ref_flux_data):
        sim_scat.load_minus_flux_data(flux_obj, flux_data)
    if stop_strategy == "fixed":
        # Diagnostic/probe fixed run: bounded, not necessarily physically converged.
        sim_scat.run(until=fixed_run_until)
    else:
        sim_scat.run(
            until_after_sources=mp.stop_when_fields_decayed(
                decay_check_interval, mp.Ez, box_center, decay_threshold
            )
        )
    freqs = mp.get_flux_freqs(flux_scat[0])
    surface_fluxes = [np.array(mp.get_fluxes(flux_obj)) for flux_obj in flux_scat]
    particle_induced_flux_relative = np.sum(surface_fluxes, axis=0)
    sim_scat.reset_meep()
    return freqs, particle_induced_flux_relative, flux_surface_names(), surface_fluxes


